import { z } from "zod"

export type RegisterFormDataType = z.infer<typeof registerFormSchema>

export const registerFormSchema = z.object({
  username: z
    .string()
    .min(3, { message: 'O usu치rio precisa ter pelo menos 3 letras.' })
    .regex(/^([a-z\\-]+)$/i, {
      message: 'O usu치rio pode ter apenas letras e hifens.',
    })
    .transform((username: string) => username.toLowerCase()),
  name: z
    .string()
    .min(3, { message: 'O nome precisa ter pelo menos 3 letras.' }),
})

const claimUsernameFormSchema = z.object({
  username: z
    .string()
    .min(3, { message: 'O usu치rio precisa ter pelo menos 3 letras.' })
    .regex(/^([a-z\\-]+)$/i, {
      message: 'O usu치rio pode ter apenas letras e hifens.',
    })
    .transform((username) => username.toLowerCase()),
})

type ClaimUsernameFormData = z.infer<typeof claimUsernameFormSchema>